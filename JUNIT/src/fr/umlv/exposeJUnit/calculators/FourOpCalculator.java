package fr.umlv.exposeJUnit.calculators;

/**
 *
 * @author doghmen
 */
public class FourOpCalculator {

        public int add(int a, int b) {
            return a + b;
        }
        
        public int sub(int a, int b) {
            return a - b;
        }

        public int mul(int a, int b) {
            return a * b;
        }

        public int div(int a, int b) {
            return a / b;
        }
}
